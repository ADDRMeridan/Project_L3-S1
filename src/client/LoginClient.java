    private void boutonconnexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonconnexionActionPerformed
        try {
            if (network.connect(idfield.getText(), mdpfield.getPassword().toString())){
                this.setVisible(false);
                javax.swing.JFrame newframe = new InterfaceUtilisateur(network);
                newframe.setExtendedState(JFrame.MAXIMIZED_BOTH);
                newframe.setVisible(true);
                this.dispose();
            }
            else{
                erreurlabel.setForeground(Color.red);
                erreurlabel.setText("Erreur de connexion : Identifiant inexistant ou mot de passe erron√©.");
            }
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(LoginClient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_boutonconnexionActionPerformed

